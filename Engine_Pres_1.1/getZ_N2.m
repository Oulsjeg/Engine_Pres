function Z  = getZ(T, P)
    
    Pc          = 492.314;
    Tc          = 126.2;
    
    if T >= 200 && T <= 320
        
        T           = T / Tc;
        P           = P / Pc; 
    
        theta       = [1.63012223077970;    ...
                       0.138625715282547;   ...
                       -2.45810643634628;   ...
                       5.22784814059470;    ...
                       -14.8841808094681;   ...
                       7.04525958511756;    ...
                       8.37669450091987;    ...
                       -1.57575831188477;   ...
                       -4.13565693056018;   ...
                       1.33892641019662];

        alpha       = [1;2;1.50000000000000;...
                       1;0.315803897810539; ...
                       0.511993730588094;   ...
                       0.0504865825410918;  ...
                       2.77659221262347;    ...
                       0.544262983881325;   ...
                       2.90591324280338];
   
    
        N           = [1;                       ...
                        T^alpha(2)*sqrt(P);     ...
                        T^alpha(3)*sqrt(P);     ...
                        T^alpha(4)*sqrt(P);     ...
                        T^alpha(5)*P;           ...
                        T^alpha(6)*P;           ...
                        T^alpha(7)*P;           ...
                        T^alpha(8);             ...
                        P^alpha(9);             ...
                        T^alpha(10)];               

        Z       = sum(theta .* N);
    end     
end